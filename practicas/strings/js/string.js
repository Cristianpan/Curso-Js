/**
 * Para declarar un string se puede utilizar " o ', tanto en apertura y cierre.
 */

//Forma más común de declaración a string (tipo primitivo)
const producto = "Monitor";

//Cast a string (tipo primitivo)
const producto2 = String('Celular');

//Creacion de un objeto de tipo string (no primitivo)
const producto3 = new String("Tableta");

console.log(producto);
console.log(producto2);
console.log(producto3);


